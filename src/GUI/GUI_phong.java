    private void bTSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTSAVEActionPerformed
        // TODO add your handling code here:
        DTO.DTO_room r =new DTO_room();
        r.setMaphong(txt_maphong.getText().trim());
        try {
            r.setGiaphong(Double.parseDouble(txt_giaphong.getText()));
        } catch (Exception e) {
            thongbao.thongbao("Giá phòng nhập sai dữ liệu", "Thông báo");
        }
         
         r.setLoaiphong(txt_loaiphong.getText());
         r.setTinhtrang((String) chontt.getSelectedItem());
       
             if(DAO.DAO_phong.suaphong(r)!=-1)
            {
            
            thongbao.thongbao("Sửa thành công", "Thông Báo");
            
             }
            else{
              thongbao.thongbao("Sửa phòng không thành công", "Thông Báo");
            }
         BLL.BLL_dodulieuPhong.dodulieuPhong(tbl_phong);
        
    }//GEN-LAST:event_bTSAVEActionPerformed

