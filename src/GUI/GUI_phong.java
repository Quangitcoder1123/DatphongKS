    private void btADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btADDActionPerformed
        // TODO add your handling code here:
         DTO.DTO_room r =new DTO_room();
         boolean ktgia=true; //kiem tra gia nhap dung dulieu
         r.setMaphong(txt_maphong.getText().trim());
         try {
            r.setGiaphong(Double.parseDouble(txt_giaphong.getText()));
        } catch (Exception e) {
            thongbao.thongbao("Giá phòng nhập sai dữ liệu", "Thông báo");
            ktgia=false;
        }
         r.setLoaiphong(txt_loaiphong.getText());
         r.setTinhtrang((String) chontt.getSelectedItem());
         if(BLL.BLL_phong.addphong(r)&& ktgia)
         {
             if(DAO.DAO_phong.addphong(r)!=-1)
            {
             BLL.BLL_dodulieuPhong.dodulieuPhong(tbl_phong);
            thongbao.thongbao("Thêm phòng thành công", "Thông Báo");
            
             }
            else{
              thongbao.thongbao("Thêm phòng không thành công", "Thông Báo");
            }
         }
         
    }//GEN-LAST:event_btADDActionPerformed

